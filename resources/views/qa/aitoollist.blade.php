    <script>
        document.querySelectorAll('.favorite').forEach(function(element) {
            element.addEventListener('click', function() {
                this.classList.toggle('favorited');
            });
        });
        
        document.querySelectorAll('.like').forEach(function(element) {
            element.addEventListener('click', function() {
                let likeCountElement = this.nextElementSibling;
                let currentLikes = parseInt(likeCountElement.innerText);
                
                this.classList.toggle('liked');
                
                if (this.classList.contains('liked')) {
                    currentLikes++;
                } else {
                    currentLikes--;
                }
                
                likeCountElement.innerText = currentLikes;
                this.setAttribute('data-likes', currentLikes);
            });
        });
    </script>
    
